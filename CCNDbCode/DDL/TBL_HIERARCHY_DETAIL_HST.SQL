  CREATE TABLE HIERARCHY_DETAIL_HST 
   (	HRCHY_HDR_NAME VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	HRCHY_DTL_LEVEL VARCHAR2(2 BYTE) NOT NULL ENABLE, 
	HRCHY_DTL_PREV_LVL_VAL VARCHAR2(1000 BYTE) NOT NULL ENABLE, 
	HRCHY_DTL_CURR_LVL_VAL VARCHAR2(1000 BYTE) NOT NULL ENABLE, 
	HRCHY_DTL_NEXT_LVL_VAL VARCHAR2(1000 BYTE), 
	HRCHY_DTL_EFF_DATE DATE NOT NULL ENABLE, 
	HRCHY_DTL_EXP_DATE DATE, 
	HRCHY_DTL_DESC VARCHAR2(100 BYTE), 
	HRCHY_DTL_CURR_ROW_VAL VARCHAR2(100 BYTE), 
	UPPER_LVL_VER_VALUE SYS.XMLTYPE 
   );

   COMMENT ON COLUMN HIERARCHY_DETAIL_HST.HRCHY_DTL_CURR_ROW_VAL IS 'This will contain the value for the current level of the  hierarchy.  This will not be the composite value in stored in HRCHY_DTL_CURR_LVL_VAL';
   COMMENT ON COLUMN HIERARCHY_DETAIL_HST.UPPER_LVL_VER_VALUE IS 'This field is used to hold the value for verification of the previous level and must exist in our Code_Detail table.';
