BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE DESCARTES_ADRS_FEED_CC';
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE DESCARTES_ADRS_FEED_CC (
    COST_CENTER_CODE VARCHAR2(6),
    TRIGGERED_DATE   DATE,
    TRIGGER_ACTION   VARCHAR2(10) NOT NULL,
    PROCESS_DATE     DATE,
    REQUEST          CLOB,
    RESPONSE         CLOB,
    GUID             VARCHAR2(40) NOT NULL,
    CONSTRAINT DESCARTES_ADRS_CC_PK PRIMARY KEY (COST_CENTER_CODE, TRIGGERED_DATE),
    CONSTRAINT DESCARTES_ADRS_CC_UNQ_GUID UNIQUE (GUID),
    CONSTRAINT DESCARTES_ADRS_CC_TRG_CHK CHECK (TRIGGER_ACTION IN ('INSERT','UPDATE')));
