DECLARE
CURSOR CUR IS
SELECT COST_CENTER_CODE 
   FROM COST_CENTER
  WHERE COST_CENTER_CODE in ('715269','715645', '718090', '708806','708823','708824', '708825','708826', '708829');
BEGIN
FOR REC IN CUR LOOP
BEGIN
  COMMON_TOOLS.DELETE_COST_CENTER(REC.COST_CENTER_CODE);
END;
END LOOP;
END;