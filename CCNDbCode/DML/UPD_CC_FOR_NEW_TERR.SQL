SELECT *
  FROM COST_CENTER
  WHERE CATEGORY = 'T' 
   AND CLOSE_DATE IS NULL 
   AND POS_DOWNLOAD_CC_IND IS NULL;


UPDATE COST_CENTER 
   SET POS_DOWNLOAD_CC_IND = 'Y' 
 WHERE CATEGORY = 'T' 
   AND CLOSE_DATE IS NULL 
   AND POS_DOWNLOAD_CC_IND IS NULL;

COMMIT;