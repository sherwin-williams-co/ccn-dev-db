-- Cost center 75Q928 Cost center name is getting changed
-- Run this in COSTCNTR

SELECT * 
  FROM COST_CENTER 
 WHERE COST_CENTER_CODE ='75Q928';


SET SERVEROUTPUT ON
DECLARE

    OUT_ID     NUMBER;

BEGIN 

    POS_DATA_GENERATION.SET_FLAG_POLLING_BULK_LOADS('MANUAL-CC-75Q928', OUT_ID);


    UPDATE COST_CENTER 
       SET COST_CENTER_NAME ='SOUTHWEST FLORIDA METRO RES REPAINT' 
     WHERE COST_CENTER_CODE ='75Q928';

    POS_DATA_GENERATION.RESET_FLAG_POLLING_BULK_LOADS(OUT_ID);

    COMMIT;

END;	 