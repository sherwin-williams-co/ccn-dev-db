--Below scripts will manually update the Paid date and the paid indicator 
--nxk927 12/17/2015 
/*----------------------------
Update: 

Draft Check Serial Number: 

0890410004 
0890410012 
0890410020 
0890410038 

with paid date of 4/4/2016 
all the indicators are N besides Open indicator which is 'Y'
----------------------------*/
SELECT * FROM STORE_DRAFTS WHERE CHECK_SERIAL_NUMBER = '0890410004'; --Picked by 1099 on 01-MAR-2016 and FSS not sent
UPDATE STORE_DRAFTS 
   SET PAY_INDICATOR = 'Y', 
       PAID_DATE = '04-APR-2016',
       BANK_PAID_AMOUNT = DECODE(AMOUNT_CHANGE_DATE, NULL, ORIGINAL_NET_AMOUNT,NET_AMOUNT)
 WHERE CHECK_SERIAL_NUMBER = '0890410004';

