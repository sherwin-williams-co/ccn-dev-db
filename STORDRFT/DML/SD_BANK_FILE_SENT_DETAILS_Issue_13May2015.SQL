/*
Below script will update the bank file sent indicator to Y so that it will be sent to bank
*/
SELECT *
  FROM SD_BANK_FILE_SENT_DETAILS
 WHERE CHECK_SERIAL_NUMBER  IN ('0746571447', '0820084036', '0523965267', '0122415250')
 AND TRUNC(PROCESS_DATE) = '12-MAY-2015';
--4 Row(s) Selected

UPDATE SD_BANK_FILE_SENT_DETAILS
   SET SEND_INDICATOR = 'Y'
  FROM SD_BANK_FILE_SENT_DETAILS
 WHERE CHECK_SERIAL_NUMBER  IN ('0746571447', '0820084036', '0523965267', '0122415250')
 AND TRUNC(PROCESS_DATE) = '12-MAY-2015';
--4 Row(s) Updated

SELECT *
  FROM STORE_DRAFTS
 WHERE CHECK_SERIAL_NUMBER  IN ('0746571447', '0820084036', '0523965267', '0122415250');
--4 Row(s) Selected

UPDATE STORE_DRAFTS
   SET CHANGE_DATE = '13-MAY-2015'
  FROM SD_BANK_FILE_SENT_DETAILS
 WHERE CHECK_SERIAL_NUMBER  IN ('0746571447', '0820084036', '0523965267', '0122415250');
--4 Row(s) Updated

COMMIT;
