BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE TEMP_PAID_DETAILS_SUNTRUST';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

  CREATE TABLE TEMP_PAID_DETAILS_SUNTRUST 
   (	TRANSACTION_TYPE_CODE VARCHAR2(2 BYTE), 
	PAID_BANK_NUMBER VARCHAR2(3 BYTE), 
	PAID_ACCOUNT_NUMBER VARCHAR2(10 BYTE), 
	PAID_CHECK_SERIAL_NUMBER VARCHAR2(10 BYTE), 
	PAID_DATE_PAID VARCHAR2(6 BYTE), 
	PAID_AMONUT_ITEMS VARCHAR2(10 BYTE), 
	PAID_CPCS_NUMBER VARCHAR2(9 BYTE)
   ) 
   ORGANIZATION EXTERNAL 
    ( TYPE ORACLE_LOADER
      DEFAULT DIRECTORY STORDRFT_LOAD_FILES
      ACCESS PARAMETERS
      ( RECORDS DELIMITED BY NEWLINE
        badfile STORDRFT_LOAD_FILES:'TEMP_PAID_DETAILS_SUNTRUST.bad'
        logfile STORDRFT_LOAD_FILES:'TEMP_PAID_DETAILS_SUNTRUST.log'
        discardfile STORDRFT_LOAD_FILES:'TEMP_PAID_DETAILS_SUNTRUST.dsc'
        LOAD WHEN ((2:3) = '50')
        FIELDS(TRANSACTION_TYPE_CODE(2:3) CHAR(2), 
               PAID_BANK_NUMBER           CHAR(3), 
               PAID_ACCOUNT_NUMBER        CHAR(10), 
               PAID_CHECK_SERIAL_NUMBER   CHAR(10), 
               PAID_DATE_PAID             CHAR(6), 
               PAID_AMONUT_ITEMS          CHAR(10), 
               PAID_CPCS_NUMBER           CHAR(9)
                            )
                        )
      LOCATION
       ( 'STBD0101_SUNTRUST_PAID.TXT'
       )
    );
