SET SERVEROUTPUT ON
BEGIN   
    FOR J IN (SELECT DISTINCT COST_CENTER_CODE, PROCESS_DATE
                FROM POS_NEW_STORES_LOAD_LIST
              )
    LOOP
        INSERT INTO POS_NEW_STORES_LOAD_LIST  
        (
          COST_CENTER_CODE
        , APPLICATION_NAME
        , POS_ID
        , PROCESS_DATE
        , PROCESSED_IND
        )
        SELECT DISTINCT J.COST_CENTER_CODE, 'PrimeSub', NULL, J.PROCESS_DATE, 'Y' 
          FROM POS_NEW_STORES_LOAD_LIST;
    END LOOP;  
    COMMIT;

EXCEPTION  
    WHEN OTHERS THEN 
        NULL;
END;