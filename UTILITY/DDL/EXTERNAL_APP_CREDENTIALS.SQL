CREATE TABLE EXTERNAL_APP_CREDENTIALS (
APPLICATION_NAME      VARCHAR2(200),
APPLICATION_PRCS      VARCHAR2(200),
APPLICATION_USER_NAME VARCHAR2(200),
APPLICATION_PASSWORD  VARCHAR2(200),
EFFECTIVE_DATE        DATE,
EXPIRATION_DATE       DATE,
CONSTRAINT EXTERNAL_APP_CREDENTIALS_PK PRIMARY KEY (APPLICATION_NAME, APPLICATION_PRCS));
