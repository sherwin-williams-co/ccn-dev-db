CREATE TABLE POS_DOWNLOADS
(
  POS_ID              NUMBER               NOT NULL,
  COST_CENTER_CODE    VARCHAR2(6)          NOT NULL,
  DATA_SOURCE         VARCHAR2(30)         NOT NULL,
  DATA_OPERATION      VARCHAR2(100)        NOT NULL,
  FILE_PROCESSED      VARCHAR2(1)          NOT NULL,
  FILE_TYPE           VARCHAR2(100)        NOT NULL,
  COMMENTS            VARCHAR2(4000),
  CREATE_DT           DATE                 NOT NULL,
  FILE_NAME           VARCHAR2(100),
  POLLING_REQUEST_ID  VARCHAR2(250),
  UPDATE_DT           DATE,
		CONSTRAINT PK_POS_ID PRIMARY KEY (POS_ID)
