
  CREATE TABLE BATCH_JOB 
   (	BATCH_JOB_NAME VARCHAR2(20 BYTE) NOT NULL ENABLE, 
	BATCH_JOB_NUMBER NUMBER(12,0) NOT NULL ENABLE, 
	BATCH_JOB_STATUS VARCHAR2(20 BYTE), 
	BATCH_JOB_START_DATE DATE, 
	BATCH_JOB_END_DATE DATE, 
	BATCH_JOB_LAST_RUN_DATE DATE, 
	TRANS_STATUS VARCHAR2(20 BYTE), 
	 CONSTRAINT BATCH_JOB_PK PRIMARY KEY (BATCH_JOB_NAME, BATCH_JOB_NUMBER)
   );

   COMMENT ON COLUMN BATCH_JOB.BATCH_JOB_STATUS IS 'This field will be either ''PROCESSING'', or ''COMPLETED''';
   COMMENT ON COLUMN BATCH_JOB.TRANS_STATUS IS 'Values will be ''ERROR'' or ''SUCCESSFUL''';
   COMMENT ON TABLE BATCH_JOB  IS 'Used to log batch jobs';
