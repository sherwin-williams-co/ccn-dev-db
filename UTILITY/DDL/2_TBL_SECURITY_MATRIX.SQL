CREATE TABLE SECURITY_MATRIX(
    USER_ID    VARCHAR2(6 BYTE), 
    PASSWORD   VARCHAR2(50 BYTE), 
    ROLE_CODE  VARCHAR2(4 BYTE), 
    CONSTRAINT SECURITY_MATRIX_PK PRIMARY KEY (USER_ID, ROLE_CODE)
    CONSTRAINT SECURITY_MATRIX_FK FOREIGN KEY (ROLE_CODE) REFERENCES ROLE_DETAILS (ROLE_CODE) ENABLE
   );

