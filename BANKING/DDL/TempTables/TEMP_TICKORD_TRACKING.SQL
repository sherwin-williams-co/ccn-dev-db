DEFAULT DIRECTORY "BANKING_LOAD_FILES"
ACCESS PARAMETERS
	(  
	RECORDS DELIMITED BY NEWLINE
	BADFILE BANKING_LOAD_FILES:'TEMP_TICKORD_TRACKING.bad'
	DISCARDFILE BANKING_LOAD_FILES:'TEMP_TICKORD_TRACKING.dis'
	LOGFILE BANKING_LOAD_FILES:'TEMP_TICKORD_TRACKING.log'
