(
	ORDER_ID              VARCHAR2(100),
	ORDER_TRACKING_NUMBER VARCHAR2(100),
	EXTERNAL_ORDER_ID_GUI VARCHAR2(100)
)
ORGANIZATION EXTERNAL 
( 
